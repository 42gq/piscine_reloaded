int	ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb < 2147483647 && nb > 0)
	{
		while (nb / a != a && nb / a >= a)
			a++;
		if (nb / a == a && nb % a == 0)
			return (a);
		else
			return (0);
	}
	else
		return (0);
}
